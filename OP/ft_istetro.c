#include "libft.h"
#include "fillit.h"

int	ft_istetro(char *s)
{
	int i;
	int c;
	int l;

	i = -1;
	while (s[++i])
	{
		c = 0;
		l = 0;
		while (c < 20)
		{
			if (s[i] == '#')
			{
				if (s[i + 1] == '#')
					l++;
				if (s[i + 5] == '#')
					l++;
			}
			c++;
			i++;
		}
		if (l < 3)
			return (0);
	}
	return (1);
}
