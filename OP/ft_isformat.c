#include "libft.h"
#include "fillit.h"

int	ft_isformat(char *s)
{
	int k;
	int x;

	x = 0;
	while (*s)
	{
		k = 0;
		while (*s != '\n')
		{
			s++;
			k++;
		}
		if (k != 4)
			return (0);
		x++;
		if (*(s + 1) == '\0' || *(s + 1) == '\n')
		{
			if (x != 4)
				return (0);
			s++;
			x = 0;
		}
		s++;
	}
	return (1);
}
