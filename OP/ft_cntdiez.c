#include "libft.h"
#include "fillit.h"

int	ft_cntdiez(char *s)
{
	int i;
	int c;
	int d;

	i = -1;
	while (s[++i])
	{
		c = 0;
		d = 0;
		while (c < 20)
		{
			if (s[i] == '#')
				d++;
			if (c == 19 && d != 4)
				return (0);
			c++;
			i++;
		}
	}
	return (1);
}
