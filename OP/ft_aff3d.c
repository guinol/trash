#include "libft.h"
#include "fillit.h"

void	ft_aff3d(char ***t, int n)
{
	int i;
	int j;

	i = 0;
	while (i < n)
	{
		j = 0;
		while (j < 4)
		{
			ft_putendl(t[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
