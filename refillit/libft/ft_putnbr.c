#include "libft.h"

void	ft_putnbr(int n)
{
	int num;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = n * -1;
		}
		num = n;
		while (num > 9)
		{
			ft_putnbr(n / 10);
			num = num % 10;
		}
		ft_putchar(num + '0');
	}
}
