#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char*)s;
	while (i < n)
	{
		if ((unsigned char)c == (unsigned char)str[i])
			return ((char*)str + i);
		i++;
	}
	return (NULL);
}
