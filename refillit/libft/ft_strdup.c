#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*s;

	i = 0;
	if (!(s = (char*)malloc(sizeof(char) * ft_strlen(src) + 1)))
		return (NULL);
	while (src[i])
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
